
public class Incrementos {
	public static void main(String[] args) {
		
		int a, x, y, z;
		
		a = 10;
		a += 1; //a = a + 1;
		a -= 1; //a = a - 1;
		a *= 3; //a = a * 3
		a /= 3; //a = a / 3
		a %= 3; //a = a % 3
		
		a++; //pós atribuição
		++a; //pré atribuição
		
		x = 10;
		y = 11;
		z = (x > y)? 100 : 0;
		
	}
}
